# 오행 상생(生): key가 value를 생함 / 상극(剋): key가 value를 극함
_GENERATES = {"목": "화", "화": "토", "토": "금", "금": "수", "수": "목"}
_CONTROLS = {"목": "토", "토": "수", "수": "화", "화": "금", "금": "목"}


def _stem_relation(user_o: str, other_o: str) -> float:
    """오행 관계 가중치: 나(user_o)를 기준으로 상대 기운과의 관계."""
    if user_o == other_o:
        return 0.3                       # 비화(어깨동무)
    if _GENERATES[other_o] == user_o:
        return 1.0                       # 인성(나를 생함)
    if _GENERATES[user_o] == other_o:
        return -0.3                      # 식상(기운 설기)
    if _CONTROLS[user_o] == other_o:
        return 0.5                       # 재성(내가 다스림 → 기회)
    return -0.8                          # 관살(나를 극함 → 압박)


