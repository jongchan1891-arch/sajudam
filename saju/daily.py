def _pick(pool, key: int):
    return pool[key % len(pool)]


def _band(pools_high, pools_mid, pools_low, score: int, key: int) -> str:
    if score >= 4:
        return _pick(pools_high, key)
    if score <= 2:
        return _pick(pools_low, key)
    return _pick(pools_mid, key)


def _clamp_score(x: float) -> int:
    return max(1, min(5, int(round(x))))


def compute_daily_fortune(birth_year: int, birth_month: int, birth_day: int, on_date) -> dict:
    """생년월일(양력)과 조회 날짜로 오늘의 운세를 결정적으로 생성한다."""
    u = day_ganzi_index(birth_year, birth_month, birth_day) % 10   # 일간 index
    t60 = day_ganzi_index(on_date.year, on_date.month, on_date.day)
    tg, tj = t60 % 10, t60 % 12

    uo = CHEONGAN_OHAENG[u]
    stem_rel = _stem_relation(uo, CHEONGAN_OHAENG[tg])
    branch_rel = _stem_relation(uo, JIJI_OHAENG[tj]) * 0.5
    hap = (u % 5 == tg % 5) and u != tg                 # 천간합 (갑기·을경·병신·정임·무계)
    chung = abs(u - tg) == 6                            # 천간충 (갑경·을신·병임·정계)

    total = 3.0 + stem_rel + branch_rel + (1.2 if hap else 0) + (-1.2 if chung else 0)
    score = _clamp_score(total)

    money = _clamp_score(
        total + (1.0 if _CONTROLS[uo] == CHEONGAN_OHAENG[tg] else 0)
        + (0.5 if _CONTROLS[uo] == JIJI_OHAENG[tj] else 0)
    )
    love = _clamp_score(
        total + (1.0 if hap else 0) - (1.0 if chung else 0)
        + (0.5 if _GENERATES[CHEONGAN_OHAENG[tg]] == uo else 0)
    )
    health = _clamp_score(
        total + (0.5 if uo == CHEONGAN_OHAENG[tg] else 0)
        - (0.7 if _CONTROLS[CHEONGAN_OHAENG[tg]] == uo else 0)
    )

    key = t60 * 7 + u * 3                               # 결정적 텍스트 선택 키
    return {
        "date": on_date.isoformat(),
        "today_gapja": CHEONGAN[tg] + JIJI[tj],
        "user_ilgan": CHEONGAN[u],
        "user_ilgan_ohaeng": uo,
        "score": score,
        "stars": "★" * score + "☆" * (5 - score),
        "summary": _band(SUMMARY_HIGH, SUMMARY_MID, SUMMARY_LOW, score, key),
        "categories": {
            "재물": {"score": money, "stars": "★" * money + "☆" * (5 - money),
                     "text": _band(MONEY_HIGH, MONEY_MID, MONEY_LOW, money, key + 1)},
            "애정": {"score": love, "stars": "★" * love + "☆" * (5 - love),
                     "text": _band(LOVE_HIGH, LOVE_MID, LOVE_LOW, love, key + 2)},
            "건강": {"score": health, "stars": "★" * health + "☆" * (5 - health),
                     "text": _band(HEALTH_HIGH, HEALTH_MID, HEALTH_LOW, health, key + 3)},
        },
        "advice": _pick(ADVICE_POOL, key + 5),
    }
